                          <h2 class="titulo-destaque">Por que nos <span class="destaque">escolher?</span></h2>
                          <p>Falando de particularidades culturais, econômicas, comportamentais e de gestão, nem sempre as soluções convencionais apresentadas para o segmento cooperativista atendem as demandas das cooperativas singulares.</p>
                          <p>Pensando nessas demandas, foi fundada a FNCC – Federação Nacional das Cooperativas de Crédito em 2014. Empresa cooperativa que visa trazer soluções para suas filiadas, com foco em padronização de informações e entendimentos das resoluções impostas pelos órgãos fiscalizadores, sempre respeitando as características individuais.</p>
                          <p>Atualmente existem três frentes de atuação: Consultoria Técnica, Consultoria Jurídica e Ouvidoria Compartilhada. Além disso, a FNCC promove reuniões e Webinares com suas filiadas para discussões de assuntos que estão em voga, melhores práticas, além de ouvir as necessidades de ampliação dos serviços oferecidos.</p>
                          <p>Falamos de uma instituição pautada na democracia e interesse no crescimento sustentável de suas filiadas. Anualmente são promovidos workshops, onde os representantes das filiadas podem levar seus dirigentes, com o principal objetivo de promover debates sobre o segmento, suas fragilidades e oportunidades de negócios. Para tal, contamos sempre com a presença de atores importantes do cooperativismo; como representantes do Banco Central do Brasil, Oce´s, OCB e demais especialistas do cooperativismo.</p>
                          <p>Enfim, a FNCC fará uma ponte entre a sua singular e o sistema cooperativo. Fazendo parte dela, sua cooperativa terá voz, orientações técnicas e operacionais, além de representatividade.</p>
