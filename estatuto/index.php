                                <h2 class="text-center fw-bold mb-5">Estatuto Social da FNCC<br>Federação Nacional das Cooperativas de Crédito</h2>
                                <h5 class="fw-bold">CAPÍTULO I – DA DENOMINAÇÃO, SEDE, FORO, ÁREA, PRAZO DE DURAÇÃO E ANO SOCIAL</h5>
                                <p class="paragrafo-estatuto">Art. 1º – A FEDERAÇÃO NACIONAL DAS COOPERATIVAS DE CRÉDITO – <strong>FNCC</strong>, Sociedade Cooperativa de responsabilidade limitada, não financeira, constituída em 10/03/2014, nos termos do Artigo 6º da Lei 5.764/71, neste Estatuto Social doravante designada simplesmente <strong>FNCC</strong>, rege-se na forma da Lei e pelas demais disposições legais e regulamentares, pelo presente Estatuto e Regimento Interno, tendo:<br>

                                    I. Sede, administração e foro jurídico à Rua Voluntários da Pátria, 654, 6º andar, Sala 606, Santana, CEP: 02010-000 na cidade de São Paulo, no Estado de São Paulo.

                                    <br>II. Área de ação em todo o território brasileiro, para efeito de admissão de associadas.

                                    <br>III. Prazo de duração indeterminado.

                                    <br>IV. Exercício social de 12 (doze) meses, com início em 01 de janeiro e término em 31 de dezembro de cada ano civil.</p>
                                <h5 class="fw-bold">CAPÍTULO II – DO OBJETO E OBJETIVOS SOCIAIS</h5>
                                <p class="paragrafo-estatuto">Art. 2º- A <strong>FNCC</strong>, com base na colaboração recíproca a que se obrigam suas associadas, tem por objeto social, organizar, em comum e em maior escala, serviços de interesse das associadas, integrando e orientando suas atividades, bem como prestando-lhes serviços comuns e inerentes à sua natureza.

                                    <br>§ 1º – Para a consecução do seu objeto social, a <strong>FNCC</strong>, na medida das suas possibilidades, deve:

<br>I. Promover a difusão da doutrina cooperativista e seus princípios ao quadro social, técnico e funcional das associadas;

<br>II. Promover assistência às associadas por meio de ações relacionadas com o seu objeto social;

<br>III. Propiciar, através de convênios com entidades especializadas, públicas ou privadas, o aprimoramento técnico-profissional e capacitação cooperativista de suas associadas;

<br>IV. Firmar contratos, intermediar ou intervir junto às cooperativas de crédito e demais instituições financeiras, todas as operações de crédito e financiamento de interesse de suas associadas;

<br>V. Administrar, com eficiência os recursos obtidos de suas associadas para a manutenção da sociedade;

<br>VI. Divulgar conhecimentos técnico-cooperativistas e ou associativos e realizar atividades sociais voltadas às associadas;

<br>VII. Providenciar a perfeita manutenção e funcionamento de suas instalações e bens próprios ou disponibilizados por terceiros;

<br>VIII. Contratar ou intermediar, em benefício das associadas, serviços, tais como, consultorias técnica e jurídica, seguro de vida individual ou coletivo, previdência privada, assistência à saúde e de acidente de trabalho;

<br>IX. Contratar, em benefício das associadas interessadas e no desenvolvimento dos objetivos sociais, convênios com cooperativas ou empresas ligadas ao consumo em geral;

<br>X. Contratar, para a consecução dos seus objetivos sociais, serviços jurídicos, médicos, farmacêuticos, odontológicos, transporte em geral, culturais e sociais;

<br>XI. Desenvolver atividades de monitoramento às associadas, que assim desejarem, buscando o aprimoramento da identidade da sociedade cooperativa, sob a ótica da legislação e normas vigentes.

<br>XII. Representar as associadas junto aos Órgãos Oficiais competentes, governamentais e/ou privados;

<br>XIII. Constituir ou participar do capital de empresas ou sociedades;

<br>XIV. Instituir e manter estrutura de Ouvidoria, nos termos da legislação e normativos vigentes, devendo sua atuação ser pautada pela transparência, independência, imparcialidade e isenção, podendo exigir das Cooperativas Associadas o acesso às informações necessárias para a elaboração de resposta adequada às reclamações recebidas, com total apoio administrativo, assim como requisitar informações e documentos para o exercício de suas atividades.

<br>§ 2º A Federação Nacional das Cooperativas de Crédito de São Paulo – <strong>FNCC</strong> é dotada de legitimidade extraordinária autônoma concorrente para agir como substituta processual em defesa dos direitos coletivos de suas singulares e/ou seu associado quando a causa de pedir versar sobre atos de interesse diretos destas que detenham ação com suas operações de mercado, mediante autorização manifestada individualmente pelo associado ou por meio de Assembleia Geral que delibere sobre a propositura judicial.

<br>§ 3º – A <strong>FNCC</strong> atuará sem discriminação política, racial, religiosa ou social.</p>
                                <h5 class="fw-bold">CAPÍTULO III – DAS OPERAÇÕES</h5>
<p class="paragrafo-estatuto">Art. 3º – A <strong>FNCC</strong> realizará toda e qualquer atividade legal que esteja relacionada com o seu objeto, sempre agindo em nome das suas associadas, conforme Art. 5º da Lei 5764/71.</p>
<h5 class="fw-bold">CAPÍTULO IV – DOS ASSOCIADOS</h5>
<h6 class="fw-bold">SEÇÃO I – DA ADMISSÃO, DIREITOS, DEVERES E RESPONSABILIDADES</h6>
<p class="paragrafo-estatuto">Art. 4º – O ingresso e permanência no quadro social da <strong>FNCC</strong> são livres a todas as cooperativas de crédito estabelecidas no território brasileiro e que desejarem utilizar os serviços prestados pela entidade, desde que adiram aos propósitos sociais, concordem e preencham as condições estabelecidas neste Estatuto e no Regimento Interno e não pratiquem qualquer atividade que possa prejudicar ou colidir com os interesses e objetivos da sociedade.

<br>§ 1º – O número de associadas será ilimitado quanto ao máximo, não podendo, entretanto, ser inferior a 3 (três) cooperativas.

<br>§ 2º – A associação importa em que a associada outorgue poderes de representação à <strong>FNCC</strong> para atuar junto a órgãos públicos e privados em defesa dos interesses comuns do sistema, da entidade e de suas associadas.

<br>Art. 5º – Para associar-se, a cooperativa interessada preencherá proposta fornecida pela <strong>FNCC</strong> e fornecerá todos os documentos exigidos pelo Regimento Interno e aqueles que a Diretoria da <strong>FNCC</strong> vier a julgar necessários.

<br>§ 1º – Aprovada a proposta pela Diretoria, a cooperativa subscreverá as quotas-partes do capital nos termos e condições previstas neste estatuto.

<br>§ 2º – A subscrição das quotas-partes do capital pela associada, complementam a sua admissão na sociedade.

<br>Art. 6º – Cumprido o que dispõe o artigo anterior, a associada adquire todos os direitos e assume todos os deveres e obrigações decorrentes da Lei, deste estatuto e das deliberações da <strong>FNCC</strong>.

<br>Art. 7º – A associada tem direito a:

<br>I. Participar da Assembleia Geral, discutindo e votando os assuntos que nela forem tratados, observadas as disposições legais e estatutárias, ressalvados os casos disciplinados nos artigos 25, §1º e §4º e artigo 29 deste Estatuto;

<br>II. Propor à Diretoria ou à Assembleia Geral, medidas de interesse da <strong>FNCC</strong>, da própria associada ou do sistema local;

<br>III. Votar e ser votado para os cargos sociais, observado o disposto nos regulamentos em vigor;

<br>IV. Participar de todas as atividades que constituam o objeto da <strong>FNCC</strong>;

<br>V. Solicitar, por escrito, informações sobre os negócios da <strong>FNCC</strong> e, no mês que anteceder a realização da Assembleia Geral Ordinária, consultar, na sede da entidade, o livro de matrícula, os demonstrativos financeiros do exercício e demais documentos a serem submetidos à Assembleia Geral;

<br>VI. Demitir-se da sociedade quando lhe convier;

<br>VII. Realizar, com a <strong>FNCC</strong>, as operações que correspondam aos objetivos de cooperativa associada;

<br>VIII. Ter acesso aos regulamentos e ou regimentos internos da <strong>FNCC</strong>;

<br>IX. Beneficiar-se dos serviços que a <strong>FNCC</strong> estiver habilitada a prestar, observadas as condições estabelecidas nas normas aplicáveis;

<br>X. Gozar de todas as vantagens previstas neste Estatuto Social.

<br>Parágrafo Único – A igualdade de direitos das cooperativas singulares associadas é garantida pela <strong>FNCC</strong>, que não pode estabelecer restrições de qualquer espécie ao livre exercício dos direitos sociais, salvo os previstos neste Estatuto.

<br>Art. 8º – A associada tem o dever de:

<br>I. Subscrever e integralizar as quotas-partes do capital nos termos deste Estatuto;

<br>II. Contribuir com o rateio das despesas e encargos operacionais que forem estabelecidos para cobertura de despesas da <strong>FNCC</strong>, conforme Plano Financeiro e Proposta de Rateio aprovados na Assembleia Geral Ordinária nos termos do Art. 80 e seus parágrafos da Lei 5764/71;

<br>III. Cumprir as disposições da Lei, deste Estatuto, do Regulamento e ou Regimento Interno, bem como as deliberações da Assembleia Geral;

<br>IV. Satisfazer pontualmente seus compromissos com a <strong>FNCC</strong>, dentre os quais o de participar ativamente da sua vida societária e empreendedora;

<br>V. Concorrer com o que lhe couber, na conformidade das disposições deste Estatuto, para a cobertura dos prejuízos da sociedade;

<br>VI. Prestar à <strong>FNCC</strong> esclarecimentos relacionados com as atividades que lhe facultaram associar-se;

<br>VII. Zelar pelo patrimônio moral e material da Sociedade;

<br>VIII. Conduzir e realizar atividades de assistência técnica, educacional e social, sempre que possível, por intermédio da <strong>FNCC</strong>;

<br>IX. Estimular o cooperativismo, mantendo estreito relacionamento com as demais cooperativas e com a <strong>FNCC</strong>;

<br>X. Designar e credenciar delegados para participação em reuniões e em Assembleias Gerais da <strong>FNCC</strong>, observadas as disposições constantes neste Estatuto;

<br>XI. Informar a <strong>FNCC</strong> toda e qualquer modificação nos órgãos de administração da associada;

<br>XII. Manter as informações do cadastro na <strong>FNCC</strong> constantemente atualizadas.

<br>Art. 9º – A associada responde subsidiariamente pelos compromissos da <strong>FNCC</strong> perante terceiros, até o valor das quotas-partes de capital que subscreverem, perdurando essa responsabilidade nos casos de demissão, de eliminação ou de exclusão, até quando aprovadas as contas do exercício em que se deu o desligamento, sem prejuízo da responsabilidade solidária da associada perante <strong>FNCC</strong> e pelo prejuízo verificado nas operações sociais, considerado a proporção de sua participação nas mesmas operações.

<br>§ 1º – A responsabilidade da associada, na forma da legislação vigente, somente poderá ser invocada depois de judicialmente exigida da <strong>FNCC</strong>, salvo nos casos previstos no § 2º deste artigo.

<br>§ 2º – A cooperativa singular associada, nos termos do Artigo 264 e seguintes do Código Civil Brasileiro, responderá, solidariamente, até o limite do valor das quotas-partes que subscrever, pela insuficiência de liquidez, bem como pela inadimplência e ou qualquer outro prejuízo, que ela ou qualquer outra cooperativa associada causarem à <strong>FNCC</strong>, devendo as demais manter dispositivo estatutário que implique na instituição da referida responsabilidade.</p>                           

<h6 class="fw-bold">SEÇÃO II – DA DEMISSÃO, ELIMINAÇÃO E EXCLUSÃO</h6>
<p class="paragrafo-estatuto">Art. 10 – A demissão da cooperativa associada, que não poderá ser negada, dar-se-á unicamente a seu pedido, por escrito, e será requerida ao Diretor Presidente, sendo por este levada à Diretoria em sua primeira reunião após a data de protocolo do pedido.

<br>§ 1º – A demissão de que trata este artigo completar-se-á com a respectiva averbação mediante assinatura do termo correspondente pelos representantes legais da demissionária e da <strong>FNCC</strong>.

<br>§ 2º – O reingresso de cooperativa demissionária será aceito a qualquer momento a critério exclusivo da Diretoria, obedecidas as normas em vigor na data da solicitação de reingresso.

<br>Art. 11 – A eliminação da associada, que será aplicada em virtude de infração da lei, ou deste estatuto, será feita por decisão da Diretoria, na primeira reunião após a constatação do fato que der razão à eliminação, depois de notificação à associada infratora, sendo que os motivos que determinarem a eliminação deverão constar de termo lavrado no livro de matrícula e assinado pelo Presidente da associada e pelo Diretor Presidente da <strong>FNCC</strong>.

<br>§ 1º – Além do motivo acima, a Diretoria poderá eliminar a associada que:

<br>I. Divulgar informações relevantes, sigilosas ou inverídicas sobre a sociedade que possam prejudicá-la nas suas atividades e negócios sociais;

<br>II. Vier a exercer qualquer atividade considerada prejudicial à sociedade ou que colida com o seu objeto social;

<br>III. Houver levado a sociedade à pratica de atos judiciais para obter o cumprimento de obrigações por ele contraídas;

<br>IV. Depois de notificada, voltar a infringir dispositivo de Lei, deste Estatuto e das deliberações da Assembleia Geral;

<br>V. Praticar atos contrários ao cooperativismo e à harmonia do quadro social;
<br>VI. Ocasionar danos materiais ou morais à <strong>FNCC</strong>, às demais cooperativas singulares associadas e/ou ao sistema;

<br>VII. Deixar de cumprir, deliberadamente, os compromissos assumidos com o poder público ou com entidades privadas;

<br>§ 2º – Cópia autêntica da decisão será remetida ao interessado, por processo que comprove as datas da remessa e do recebimento, dentro do prazo máximo de 30 (trinta) dias corridos, contados da data da reunião em que se deliberou pela eliminação.

<br>§ 3º – A cooperativa associada eliminada poderá, dentro do prazo de 30 (trinta) dias corridos, contados da data de recebimento formal do termo de notificação de eliminação, interpor recurso para a primeira Assembleia Geral que se realizar após a eliminação.

<br>§ 4º – O recurso referido no parágrafo anterior será recebido pela Diretoria e terá efeito suspensivo até a data de realização da Assembleia Geral.

<br>Art. 12 – A exclusão da associada será feita:

<br>I. Por dissolução da pessoa jurídica;

<br>II. Pelo cancelamento da autorização de funcionamento expedida pelo Banco Central;

<br>III. Por deixar de atender aos requisitos estatutários de ingresso ou permanência na cooperativa.

<br>Parágrafo Único – A exclusão com fundamento nas disposições dos incisos I e II será automática e a exclusão considerando o inciso III será feita por decisão da Diretoria, observadas as diretrizes para eliminação das associadas.

<br>Art. 13 – Nos casos de demissão, eliminação ou exclusão, a associada terá direito a restituição do capital que integralizou, acrescido das sobras ou deduzido das perdas registradas.

<br>§ 1º – A restituição de que trata este artigo somente poderá ser exigido depois de aprovado, pela Assembleia Geral, o balanço do exercício em que a associada tenha sido desligada da <strong>FNCC</strong>.

<br>§ 2º – A Diretoria da <strong>FNCC</strong> poderá determinar que a restituição deste capital seja feita em parcelas, a partir do exercício financeiro que se seguir àquele em que se deu o desligamento e no mesmo prazo e condições da integralização.

<br>§ 3º – Ocorrendo demissões, eliminações ou exclusões de associadas em número tal que a restituição das importâncias referidas neste artigo possa ameaçar a estabilidade econômico-finan¬ceira da sociedade, esta poderá restituí-las mediante critérios que resguardem a sua continuidade.

<br>§ 4º – Os deveres de associada perduram para os demitidos, eliminados ou excluídos, até que sejam aprovadas pela Assembleia Geral as contas do exercício em que a associada deixou de fazer parte da sociedade.</p>
                            <h5 class="fw-bold">CAPÍTULO V – DO CAPITAL SOCIAL</h5>
<p class="paragrafo-estatuto">Art. 14 – O Capital Social da <strong>FNCC</strong> é ilimitado quanto ao máximo variando conforme o número de quotas-partes subscritas, não podendo, entretanto, ser inferior a R$ 30.000,00 (trinta mil reais).

<br>§1º – O capital é dividido em quotas-partes de valor unitário igual a R$ 500,00 (quinhentos reais), realizáveis em moeda corrente nacional.

<br>§ 2º – A quota-parte é indivisível, intransferível a não associadas, não poderá ser negociada de modo algum, nem dada em garantia, e todo o seu movimento de subscrição, realização, transferência e restituição será sempre escriturado em termo.

<br>§ 3º – As quotas-partes poderão ser transferidas total ou parcialmente entre as associadas mediante autorização prévia da Diretoria, mediante a lavratura de termo que contenha as assinaturas dos representantes legais da cedente, da cessionária e da <strong>FNCC</strong>.

<br>§ 4º- A critério da Diretoria, a associada poderá pagar as quotas-partes à vista, ou em até 05 (cinco) parcelas iguais e sucessivas, independentemente de chamada, ou por meio de contribuições.

<br>§ 5º – Sobre a subscrição ou integralização de capital feita com atraso incidirá juros de mora dentro dos limites da lei.

<br>§ 6º – Nenhuma cooperativa associada poderá deter mais de 1/3 (um terço) do capital social da <strong>FNCC</strong>.

<br>§ 7º – Dependendo do resultado econômico-financeiro, a sociedade poderá pagar juros ao capital integralizado pelas cooperativas associadas até o limite de 12% ao ano, mediante decisão da Diretoria, nos termos do art. 24, § 3º da Lei Federal n. 5.764/71.

<br>Art. 15 – Ao ser admitida na <strong>FNCC</strong>, a cooperativa deverá subscrever, no mínimo, 20 (vinte) quotas-partes do Capital So¬cial e no máximo 1/3 (um terço) do total das quotas.

<br>Art. 16 – Eventuais débitos vencidos da cooperativa singular associada poderão, a critério único e exclusivo da <strong>FNCC</strong>, ser deduzidos do montante das respectivas quotas-partes, resguardados os limites operacionais e o prescrito no Art. 13º e seus parágrafos.

<br>Parágrafo Único – Em sendo realizada a compensação de que trata o caput, a responsabilidade da cooperativa desligada, eliminada ou excluída perdurará até a aprovação de contas relativas ao exercício em que se deu a saída da sociedade.</p>
                        <h5 class="fw-bold">CAPÍTULO VI – DOS ÓRGÃOS SOCIAIS</h5>
<p class="paragrafo-estatuto">Art. 17 – A estrutura de governança corporativa da Federação é composta pelos seguintes órgãos sociais:

<br>I. Assembleia Geral;

<br>II. Diretoria; e

<br>III. Conselho Fiscal.</p>
                     <h6 class="fw-bold">SEÇÃO I – DA ASSEMBLÉIA GERAL</h6>       
                       <p class="paragrafo-estatuto">Art. 18 – A Assembleia Geral, Ordinária ou Extraordinária, é o órgão supremo da <strong>FNCC</strong> e, dentro dos limites da Lei e deste estatuto, tomará toda e qualquer decisão de interesse da sociedade e suas deliberações vinculam a todas as associadas ainda que ausentes ou discordantes.

<br>Art. 19 – A Assembleia Geral será normalmente convocada e dirigida pelo Diretor-Presidente da sociedade.

<br>Parágrafo Único – A Assembleia Geral poderá, também, ser convocada pela Diretoria, pelo Conselho Fiscal ou por 1/5 das cooperativas singulares associadas, em pleno gozo dos seus direitos, após solicitação não atendida pelo Diretor-Presidente no prazo máximo de 10 (dez) dias corridos, contados a partir da data de protocolização do pedido.

<br>Art. 20 – Em quaisquer das hipóteses referidas no artigo anterior, as Assembleias Gerais serão convocadas com antecedência mínima de 10 (dez) dias para a primeira reunião, em primeira convocação para o horário estabelecida para início, em segunda convocação após decorrida 1 (uma) hora do horário anterior, e em terceira convocação após decorrida 1 (uma hora) do horário anterior, obedecidos os limites de quorum definidos no Art. 24º.

<br>Parágrafo Único – As três convocações serão feitas em um único Edital.

<br>Art. 21 – Não havendo quorum para instalação da Assembleia convocada nos termos do artigo anterior, será feita nova convocação com antecedência mínima de 10 (dez) dias.

<br>Art. 22 – Dos Editais de Convocação das Assembleias Gerais deverá constar:

<br>I. A denominação da <strong>FNCC</strong>, seguida da expressão “Convocação da Assembleia Geral” Ordinária ou Extraordinária, conforme o caso;

<br>II. O dia e a hora da reunião, em cada convocação, assim como o endereço do local de sua realização, o qual salvo motivo justificado, será sempre o da sede social;

<br>III. A sequência ordinal das convocações e o quorum de instalação;

<br>IV. A ordem do dia dos trabalhos, com as devidas especificações e, em caso de reforma do estatuto, a indicação precisa da matéria;

<br>V. O número de associadas existentes na data da sua expedição, para efeito de cálculo do quorum de instalação;

<br>VI. O local, a data, o nome, o cargo e a assinatura do responsável pela convocação.

<br>§ 1º – Em caso de convocação por associadas, o Edital será assinado, no mínimo, por 1/5 dos representantes das solicitantes.

<br>§ 2º – Os Editais de convocação deverão:

<br>I. Ser afixados em locais visíveis das dependências da <strong>FNCC</strong> mais comumente frequentadas pelas associadas;

<br>II. Publicados em jornal de circulação regular; e,

<br>III. Transmitidos através de circular às cooperativas associadas.

<br>Art. 23 – É de competência das Assembleias Gerais, Ordinária ou Extraordinária, a destituição de membros da Diretoria e Conselho Fiscal.

<br>Parágrafo Único – Ocorrendo destituição que possa comprometer a regularidade da administração ou fiscalização da entidade, poderá a Assembleia designar administradores e conselheiros, até a posse dos novos, cuja eleição se efetuará no prazo máximo de 30 (trinta) dias contados da data da Assembleia que designou os novos gestores.

<br>Art. 24 – O quorum, para instalação da Assembleia Geral, é o seguinte, observados os intervalos de tempo indicados no Art. 19:

<br>I. 2/3 (dois terços) do número de associadas em condições de votar, em primeira convocação;

<br>II. Metade mais um dos associados, em segunda convocação;

<br>III. Qualquer número de associados em terceira e última chamada.

<br>Parágrafo Único – Para efeito de verificação do quorum de que trata este artigo, o número de associadas presentes, em cada convocação, far-se-á por suas assinaturas, seguidas dos respectivos números de matrículas, apostas no Livro de Presença.

<br>Art. 25 – Cada cooperativa singular associada será representada na Assembleia Geral da <strong>FNCC</strong> pelo respectivo Presidente ou por delegado constituído, o qual deverá apresentar no momento da assinatura no Livro de Presença, o instrumento público ou particular outorgado pela cooperativa.

<br>§ 1º – O delegado constituído deve ser integrante da administração da cooperativa e não poderá indicar procurador em nome próprio.

<br>§ 2º – O instrumento de mandato ficará anexado à Ata da Assembleia.

<br>Art. 26 – Os trabalhos das Assembleias Gerais serão dirigidos pelo Diretor-Presidente, auxiliado pelos outros membros da Diretoria Executiva.

<br>§ 1º – Na ausência do Diretor Presidente, os trabalhos serão conduzidos pelo seu substituto estatutário, e na ausência deste, por um delegado de cooperativa singular associada indicado pelos presentes.

<br>§ 2º – Quando a Assembleia Geral não tiver sido convocada pelo Presidente, os trabalhos serão dirigidos por delegado escolhido na ocasião.

<br>§ 3º – O condutor dos trabalhos poderá ser um delegado ou um funcionário da <strong>FNCC</strong> presente à Assembleia para secretariar e lavrar a ata.

<br>Art. 27 – Os ocupantes de cargos de administração, bem como quaisquer outros delegados não poderão votar nas decisões sobre assuntos que a eles se refiram, direta ou indiretamente, entre os quais os relacionados com a prestação de contas e fixação de honorários, mas não ficarão privados de tomar parte nos respectivos debates.

<br>Art. 28 – Nas Assembleias Gerais, em que forem discutidos os balanços das contas, o Presidente da Assembleia, logo após a leitura do relatório de gestão da Diretoria, das peças contábeis e do parecer do Conselho Fiscal, solicitará ao plenário que indique um associado para coordenar os debates e a votação da matéria.

<br>§ 1º – Transmitida a direção dos trabalhos, o Presidente, Diretores e Fiscais deixarão a mesa, permanecendo, contudo, no recinto à disposição da Assembleia, para os esclarecimentos que lhes forem solicitados.

<br>§ 2º – O coordenador indicado escolherá entre os delegados um secretário ad hoc, para auxiliá-lo na redação das decisões a serem incluídas na ata, pelo Secretário da Assembleia.

<br>Art. 29 – As deliberações das Assembleias Gerais somente poderão versar sobre os assuntos constantes do Edital de Convocação.

<br>§1º – Os assuntos que não constarem expressamente do Edital de Convocação e os que não satisfazerem as limitações deste artigo, somente poderão ser discutidos depois de esgotada a ordem do dia e mediante a concordância de todos os presentes. No entanto, se a matéria for objeto de decisão, será obrigatoriamente assunto para nova Assembleia Geral.

<br>§2º – Em regra, a votação será por voto aberto, mas a Assembleia poderá optar pelo voto secreto, atendendo-se, então, a regulamentação própria vigente.

<br>§3º – Os assuntos discutidos e deliberados na Assembleia Geral deverão constar de ata circunstanciada, lavrada no livro próprio, a qual após lida, deverá ser aprovada e assinada ao final dos trabalhos pelo Secretário e pelo Presidente da Assembleia, por uma comissão de 03 (três) delegados indicados pelo plenário e, ainda, por quantos o queiram fazer.

<br>§4º – As deliberações nas Assembleias Gerais serão tomadas por maioria simples de votos dos delegados presentes com direito de votar, tendo cada delegado direito a um voto, sendo vedado o voto por procuração.

<br>§5º – Devem constar, também, da ata da Assembleia Geral:

<br>I. Nomes completos, número de CPF, nacionalidade, estado civil, profissão, número da carteira de identidade, data de nascimento, endereço completo, órgãos estatutários, cargos e prazos de mandato de membros eleitos;

<br>II. Transcrição integral dos artigos reformados, no caso de alteração estatutária cuja modificação corresponda a menos de 50% (cinquenta por cento) do documento;

<br>III. Referência ao estatuto social reformado anexado à ata, no caso de alteração estatutária cuja modificação corresponda a mais de 50% (cinquenta por cento) do documento;

<br>IV. A declaração pelo secretário da Assembleia de que a ata foi lavrada em folhas soltas, quando for o caso, ou que é cópia fiel daquela lavrada em livro próprio.

<br>Art. 30 – Fica impedido de votar e ser votada nas Assembleias Gerais, a cooperativa que tenha sido admitida após sua convocação.

<br>Art. 31 – A Assembleia Geral não poderá ficar em sessão permanente.

<br>Art. 32 – É de competência exclusiva da Assembleia Geral deliberar sobre:

<br>I. Alienação ou oneração dos bens imóveis de uso próprio da sociedade;

<br>II. Destituição de membros da Diretoria e do Conselho Fiscal;

<br>III. Julgar o recurso interposto por cooperativa singular associada que não concordar com o termo de eliminação.

<br>Art. 33 – Prescreve em 4 (quatro) anos a ação para anular as deliberações da Assembleia Geral viciadas de erro, dolo, fraude ou simulação, ou tomadas com violação da Lei ou de Estatuto contado o prazo da data em que a Assembleia tiver sido realizada.</p>     
                   <h6 class="fw-bold">SEÇÃO II – DA ASSEMBLÉIA GERAL ORDINÁRIA</h6>        
                      <p class="paragrafo-estatuto">Art. 34 – A Assembleia Geral Ordinária realizar-se-á obrigatoriamente uma vez por ano, no decorrer dos 3 (três) primeiros meses após encerramento do exercício social e deliberará sobre os seguintes assuntos, que deverão constar da ordem do dia:

<br>I. Prestação de conta dos órgãos de administração, acompanhada do parecer do Conselho Fiscal, compreendendo:
<br>a) Relatório de Gestão;
<br>b) Balanço Patrimonial;
<br>c) Demonstrativo das Sobras apuradas ou das perdas decorrentes da insuficiência das contribuições para cobertura das despesas da Sociedade e do parecer do Conselho Fiscal.

<br>II. Destinação das sobras apuradas ou rateio das perdas decorrentes da insuficiência das contribuições para cobertura das despesas da Sociedade, deduzindo-se, no primeiro caso, as parcelas para os fundos obrigatórios;

<br>III. Estabelecimento da fórmula de cálculo a ser aplicada na distribuição das sobras ou no rateio das perdas;

<br>IV. Eleição dos componentes da Diretoria, quando for o caso, e do Conselho Fiscal;

<br>V. Aprovação do Plano Financeiro e Proposta de Rateio;

<br>VI. Fixação do valor dos honorários, pró-labore ou verbas de representação para os membros da Diretoria, bem como o da Cédula de Presença, para os membros do Conselho Fiscal, pelo comparecimento às respectivas reuniões;

<br>VII. Quaisquer assuntos de interesse social, excluídos os enumerados no artigo 36 deste Estatuto, desde que mencionados no respectivo Edital.

<br>§1º – A aprovação da prestação de contas da Diretoria não desonera de responsabilidade os administradores e os membros do Conselho Fiscal.

<br>§2º – Os membros dos órgãos de Administração e Fiscalização não poderão participar da votação das matérias referidas nos itens I e IV deste artigo.</p> 
                 <h6 class="fw-bold">SEÇÃO III – DA ASSEMBLÉIA GERAL EXTRAORDINÁRIA</h6>      
                 <p class="paragrafo-estatuto">Art. 35 – A Assembleia Geral Extraordinária realizar-se-á sempre que necessária e poderá deliberar sobre qualquer assunto de interesse da Sociedade desde que mencionado no Edital de Convocação.

<br>Art. 36 – É da competência exclusiva da Assembleia Geral Extraordinária deliberar sobre os seguintes assuntos:

<br>I. Reforma de Estatuto;

<br>II. Fusão, incorporação ou desmembramento;

<br>III. Mudança do objeto da Sociedade;

<br>IV. Dissolução voluntária da Sociedade e nomeação de liquidantes;

<br>V. Prestação de contas do liquidante.

<br>Parágrafo Único – São necessários os votos de 2/3 (dois terços) das cooperativas singulares associadas presentes para tornar válidas as deliberações de que trata este artigo.</p>
                       <h6 class="fw-bold">SEÇÃO IV – DA DIRETORIA</h6>
                       <p class="paragrafo-estatuto">Art. 37 – A <strong>FNCC</strong> será administrada por uma Diretoria composta de 03 (três) membros, eleitos pela Assembleia Geral para um mandato de 03 (três) anos, com os cargos de Diretor-Presidente, Diretor-Financeiro e Diretor-Administrativo.

<br>§1º – Os eleitos investirão em seus cargos imediatamente após o registro da ata da assembleia que os elegeu.

<br>§2º- Os administradores, eleitos ou contratados, não serão pessoalmente responsáveis pelas obrigações que contraírem em nome da sociedade, mas responderão solidariamente pelos prejuízos resultantes de seus atos, se agirem com culpa ou dolo.

<br>§3º – A <strong>FNCC</strong> responderá pelos atos a que se refere o parágrafo anterior, se os houver ratificado ou deles logrado proveito.

<br>§4º – Os que participarem de ato ou operação social em que se oculte a natureza da sociedade podem ser declarados pessoalmente responsáveis pelas obrigações em nome dela contraídas, sem prejuízos das sanções penais cabíveis.

<br>§5º – A Diretoria poderá deliberar pela contratação de dirigente que não seja membro de qualquer cooperativa associada.

<br>§6º – A Assembleia Geral poderá destituir os membros da Diretoria a qualquer tempo.

<br>§7º – Os componentes da Diretoria e dirigentes contratados equiparam-se aos administradores das sociedades anônimas, para efeito de responsabilidade criminal.

<br>§ 8º – Sem prejuízo da ação que possa caber a qualquer cooperado, a <strong>FNCC</strong>, por seus dirigentes, ou representada pela associada escolhida em Assembleia Geral, terá direito da ação contra os administradores, para promover a sua responsabilidade.

<br>§9º – Os membros da Diretoria respondem solidariamente pelas obrigações assumidas durante a gestão, até que se cumpram.

<br>Art. 38 – A Diretoria reúne-se ordinariamente uma vez por mês, em dia e hora previamente marcados, e extraordinariamente sempre que necessário, por convocação do Diretor-Presidente, ou da maioria de seus membros, ou, ainda, por solicitação do Conselho Fiscal.

<br>§ 1º – As reuniões da Diretoria serão realizadas mediante a presença de no mínimo metade mais um dos seus membros.

<br>§ 2º – As deliberações serão tomadas pela maioria simples dos votos dos membros presentes.

<br>§ 3º – Em caso de empate, caberá ao Diretor-Presidente desempatar.

<br>§ 4º – As deliberações serão consignadas em atas circunstanciadas, lavradas em livro próprio, lidas, aprovadas e assinadas ao final dos trabalhos pelos membros presentes, podendo ser adotado livro de folhas soltas.</p>
                       <h6 class="fw-bold">SEÇÃO V – DAS AUSÊNCIAS, DOS IMPEDIMENTOS E DA VACÂNCIA DE CARGOS DA DIRETORIA</h6>
                       <p class="paragrafo-estatuto">Art. 39 – Nas ausências ou impedimentos temporários inferiores a 60 (sessenta) dias corridos, o Presidente será substituído pelo Diretor-Administrativo, e este pelo Diretor-Financeiro.

<br>Art. 40 – Nos casos de impedimentos superiores a 60 (sessenta) dias corridos ou de vacância do cargo de Diretor-Presidente, a Diretoria designará substituto escolhido entre seus membros.

<br>Art. 41 – Ficando definitivamente vago, por qualquer tempo, qualquer dos cargos da Diretoria, deverá o Diretor-Presidente, ou seu substituto, ou os membros restantes, ou, ainda o Conselho Fiscal, no prazo de 30 (trinta) dias contados da ocorrência, convocar Assembleia Geral para o preenchimento dos cargos vagos.

<br>Art. 42 – Os substitutos exercerão os cargos somente até o final do mandato dos substituídos.

<br>Art. 43 – Constituem, entre outras, hipóteses de vacância automática do cargo eletivo:

<br>I. Morte;

<br>II. Renúncia;

<br>III. Destituição;

<br>IV. Perda da condição de cooperado de cooperativa singular associada;

<br>V. Não comparecimento, sem a devida justificativa a 3 (três) reuniões ordinárias consecutivas ou a 6 (seis) alternadas durante o exercício social.</p>
                       <h6 class="fw-bold">SEÇÃO VI – DAS COMPETÊNCIAS DA DIRETORIA</h6>
                       <p class="paragrafo-estatuto">Art. 44 – Compete à Diretoria, dentro dos limites da Lei e deste Estatuto, atendidas as decisões ou recomendações da Assembleia Geral:

<br>I. Planejar, fixar normas e aprovar orçamentos e planos de trabalho para as operações e serviços da <strong>FNCC</strong>, controlando os resultados obtidos;

<br>II. Aprovar as políticas administrativas, de crédito, de gestão de recursos financeiros e de gerenciamento de riscos;

<br>III. Verificar mensalmente a situação econômico-financeira da <strong>FNCC</strong>;

<br>IV. Deliberar sobre a admissão, a eliminação e a exclusão de cooperativas singulares associadas, podendo, a seu exclusivo critério, aplicar, por escrito, advertência prévia;

<br>V. Deliberar sobre a forma e o prazo de resgate das quotas-partes de cooperativas singulares associadas, inclusive se parcial;

<br>VI. Deliberar sobre a convocação da Assembleia Geral;

<br>VII. Propor à Assembleia Geral Extraordinária alteração no estatuto social;

<br>VIII. Deliberar sobre compra e venda de bens imóveis destinados ao uso próprio da <strong>FNCC</strong>;

<br>IX. Deliberar sobre alocação e aplicação dos recursos do Fundo de Assistência Técnica, Educacional e Social (FATES), respeitado o regulamento próprio;

<br>X. Definir sobre o pagamento de juros ao capital das cooperativas singulares, em sua forma e valor, respeitados os limites legais.

<br>XI. Deliberar pela contratação de auditor externo, se necessário;

<br>XII. Publicar os normativos internos da <strong>FNCC</strong>;

<br>XIII. Estabelecer normas internas em casos omissos e, se for o caso, submetê-las à deliberação da Assembleia Geral;

<br>XIV. Examinar as denúncias de irregularidades praticadas no âmbito da <strong>FNCC</strong>, especialmente as que lhes forem encaminhadas pelo Conselho Fiscal e pela Auditoria, e determinar medidas visando às devidas apurações e as providências cabíveis;

<br>XV. Acompanhar e adotar providências necessárias para o cumprimento do Planejamento Estratégico aprovado;

<br>XVI. Acompanhar e adotar medidas de saneamento dos apontamentos da Auditoria Interna, da Auditoria Externa e da área de Controle Interno;

<br>XVII. Examinar e deliberar sobre as propostas relativas ao plano de cargos e salários, estrutura organizacional da <strong>FNCC</strong>, regimentos e regulamentos internos;

<br>XVIII. Propor a revisão do valor estipulado para subscrição e integralização de quotas de capital;

<br>XIX. Delegar outras competências a seus membros;

<br>XX. Programar as operações e serviços estabelecendo qualidade e fixando quantidades, valores, prazos, taxas, encargos e demais condições necessárias à sua efetivação;

<br>XXI. Estabelecer, em instruções ou regulamentos, sanções ou penalidades a serem aplicadas nos casos de violação ou abuso come¬tido contra disposições da Lei, deste Estatuto ou das regras de relacionamento com a Sociedade, que vierem a ser expedidas de suas reuniões;

<br>XXII. Aprovar o Planejamento Financeiro Anual, determinando receitas e despesas da <strong>FNCC</strong>;

<br>XXIII. Contratar e fixar normas para admissão e demissão de empregados;

<br>XXIV. Fixar as normas de disciplina funcional;

<br>XXV. Julgar os recursos formulados pelos empregados contra decisões disciplinares;

<br>XXVI. Contratar, quando necessário, serviço de auditoria independente;

<br>XXVII. Contrair obrigações, transigir, adquirir, alienar e onerar bens móveis, ceder direitos e constituir mandatários;

<br>XXVIII. Definir as taxas aplicadas aos juros ao capital, observada a legislação vigente.

<br>Parágrafo Único – As normas estabelecidas pela Diretoria serão baixadas em forma de resolução ou instrução e constituirão o regimento interno da Cooperativa.

<br>Art. 45 – São atribuições do Diretor Presidente:

<br>I. Convocar e presidir as reuniões de Diretoria;

<br>II. Facilitar e conduzir os debates dos temas nas reuniões de Diretoria;

<br>III. Tomar votos e votar nas deliberações de Diretoria, respeitado o regulamento próprio;

<br>IV. Convocar a Assembleia Geral e presidi-la;

<br>V. Proporcionar, aos demais membros da Diretoria, conhecimento prévio dos assuntos a serem discutidos nas reuniões;

<br>VI. Assegurar que todos os membros da Diretoria tenham direito a se manifestar com independência, sobre qualquer matéria colocada em votação;

<br>VII. Decidir, “ad referendum” da Diretoria, sobre matéria urgente e inadiável, submetendo a decisão à deliberação do colegiado, na primeira reunião ordinária subsequente ao ato;

<br>VIII. Permitir, excepcionalmente, a inclusão de assuntos extra pauta, considerando a relevância e a urgência do assunto;

<br>IX. Salvaguardar e cumprir as demais atribuições apresentadas em regulamento próprio;

<br>X. Designar responsável para organizar, secretariar e administrar as reuniões da Diretoria, respeitado o regulamento próprio;

<br>XI. Aplicar as advertências estipuladas pela Diretoria;

<br>XII. Representar a <strong>FNCC</strong>, passiva ou ativamente, em juízo ou fora dele;

<br>XIII. Conduzir o relacionamento com terceiros no interesse da <strong>FNCC</strong>;

<br>XIV. Representar a Diretoria nas apresentações e na prestação de contas para a Assembleia Geral;

<br>XV. Informar, tempestivamente, a Diretoria a propósito de fatos que requeiram medidas urgentes;

<br>XVI. Decidir, em conjunto com os demais membros da Diretoria, sobre a admissão e demissão de funcionários e/ou contratação e encerramento de contratos de prestação de serviços com terceiros;

<br>XVII. Executar outras atividades não previstas neste Estatuto Social, determinadas pela Diretoria ou pela Assembleia Geral.

<br>XVIII. Zelar pelos serviços de ouvidoria, bem como pelo atendimento das denúncias, reclamações.

<br>Art. 46 – São atribuições do Diretor Administrativo:

<br>I. Dirigir as atividades administrativas no que tange às políticas de recursos humanos, tecnológicos e materiais;

<br>II. Executar as políticas e diretrizes de recursos humanos, tecnológicos e materiais;

<br>III. Orientar e acompanhar a contabilidade da cooperativa, de forma a permitir uma visão permanente da sua situação econômica, financeira e patrimonial;

<br>IV. Zelar pela eficiência, eficácia e efetividade dos sistemas informatizados e de telecomunicações;

<br>V. Decidir, em conjunto com o Diretor Presidente, sobre a admissão e a demissão de pessoal;

<br>VI. Coordenar o desenvolvimento das atividades sociais e sugerir à Diretoria as medidas que julgar conveniente;

<br>VII. Lavrar ou coordenar a lavratura das atas das Assembleias Gerais e das reuniões da Diretoria;

<br>VIII. Assessorar o Diretor Presidente nos assuntos de sua área;

<br>IX. Orientar, acompanhar e avaliar a atuação do pessoal de sua área;

<br>X. Substituir o Diretor Presidente e o Diretor Financeiro;

<br>XI. Desenvolver outras atribuições que lhe sejam conferidas pela Diretoria;

<br>XII. Resolver os casos omissos, em conjunto com o Diretor Presidente.

<br>Art. 47 – São atribuições do Diretor Financeiro:

<br>I. Acompanhar a movimentação financeira em geral e sugerir a Diretoria as medidas ou providências que julgar conveniente;

<br>II. Supervisionar os serviços de contabilidade;

<br>III. Supervisionar os serviços de caixa e tesouraria;

<br>IV. Elaborar a programação financeira e o fluxo de caixa;

<br>V. Manter o “encaixe” dentro de limites adequados;

<br>VI. Zelar pela segurança dos recursos e outros valores mobiliários;

<br>VII. Assinar, conjuntamente com o Diretor Presidente ou na ausência deste, com o Diretor Administrativo, os cheques emitidos pela Cooperativa, os instrumentos de procuração e os contratos com terceiros e, individualmente, se necessário endossar os cheques para depósito bancário;

<br>VIII. Implementar as ações recomendadas pelo Conselho Fiscal, auditorias e assessorias, afetas a sua área de atuação;

<br>IX. Auxiliar no desenvolvimento das atividades administrativas e sugerir as medidas que julgar conveniente;

<br>X. Auxiliar e dar suporte na preparação das prestações de contas para as Assembleias Gerais e as reuniões da Diretoria;

<br>XI. Preparar e apresentar os documentos e relatórios contábeis/ gerenciais com pelo menos uma semana de antecedência para reunião do Conselho Fiscal;

<br>XII. Atendimento das auditorias e fiscalização;

<br>XIII. Acompanhar as obrigações fiscais fazendo com que a cooperativa não fique vulnerável perante o seu propósito de constituição;

<br>XIV. Acompanhar o planejamento, o orçamento e o relatório de atividades;

<br>XV. O coordenar e controlar todos os projetos e atividades afetos a sua área de atuação;

<br>XVI. Cumprir e fazer cumprir as deliberações e decisões da Diretoria;

<br>XVII. Responder pelos assuntos e ocorrências da área que dirige.</p>
                       <h6 class="fw-bold">SEÇÃO VII – DA INVESTIDURA E DO EXERCÍCIO DOS CARGOS DE ADMINISTRAÇÃO</h6>
                       <p class="paragrafo-estatuto">Art. 48 – Os membros dos órgãos de administração da <strong>FNCC</strong> serão investidos em seus cargos mediante termos de posse lavrados no Livro de Atas e permanecerão em exercício até a posse dos substitutos.

<br>Parágrafo Único – Os eleitos serão empossados em até, no máximo, 30 (trinta) dias contados da data da Assembleia Geral que os elegeu.</p>
                       <h6 class="fw-bold">SEÇÃO VIII – DO CONSELHO FISCAL</h6>
                       <p class="paragrafo-estatuto">Art. 49 – A Administração da <strong>FNCC</strong> será fiscalizada assídua e minuciosamente por um Conselho Fiscal, constituído de 03 (três) membros efetivos e 03 (três) suplentes, todos pertencentes a administração das cooperativas associadas, eleitos anualmente pela Assembleia Geral, sendo permitida apenas a reeleição de 1/3 (um terço) dos seus componentes.

<br>§1º – Não podem fazer parte do Conselho Fiscal, além dos inelegíveis enumerados no Artigo 66 deste Estatuto, os parentes dos membros da Diretoria até 2º (segundo) grau em linha reta ou colateral, afins e cônjuge, bem como os parentes entre si até esse grau.

<br>§2º – A associada não pode exercer cumulativamente cargos na Diretoria e no Conselho Fiscal.

<br>Art. 50 – Os componentes do Conselho Fiscal equiparam-se aos administradores das sociedades anônimas para efeito de responsabilidade criminal.

<br>Art. 51 – Os membros efetivos do Conselho Fiscal são solidariamente responsáveis pelos atos e fatos irregulares praticados pela administração da <strong>FNCC</strong>, desde que, no exercício da fiscalização, revelem-se omissos, displicentes e com ausência de acuidade de pronta advertência à Diretoria e, na inércia desta, de oportuna e conveniente denúncia à Assembleia Geral.

<br>Parágrafo Único – A responsabilidade solidária se circunscreverá ao montante dos prejuízos causados.</p>
                       <h6 class="fw-bold">SEÇÃO IX – DA INVESTIDURA E DO EXERCÍCIO DOS CARGOS DO CONSELHO FISCAL</h6>
                       <p class="paragrafo-estatuto">Art. 52 – Os membros do Conselho Fiscal serão investidos em seus cargos mediante termos de posse lavrados no Livro de Atas de assembleias e permanecerão em exercício até a posse dos seus substitutos.

<br>Parágrafo Único – Os eleitos serão empossados em até, no máximo, 30 (trinta) dias, contados da data da Assembleia Geral que os elegeu.

<br>Art. 53 – A Assembleia Geral poderá destituir os membros do Conselho Fiscal a qualquer tempo.

<br>Art. 54 – Para efeito da investidura e do exercício de cargos do Conselho Fiscal, aplicam-se as condições de elegibilidade dispostas no Artigo 66 deste estatuto.</p>
                       <h6 class="fw-bold">SEÇÃO X – DAS AUSÊNCIAS, DOS IMPEDIMENTOS E DA VACÂNCIA DO CONSELHO FISCAL</h6>
                       <p class="paragrafo-estatuto">Art. 55 – Constituem, entre outras, hipóteses de vacância automática do cargo eletivo:

<br>I. Morte;

<br>II. Renúncia;

<br>III. Destituição;

<br>IV. Não comparecimento, sem a devida justificativa, a 03 (três) reuniões consecutivas ou a 6(seis) alternadas durante o exercício social;

<br>V. Perda da condição de cooperado da cooperativa singular associada.

<br>Parágrafo Único – Para que não haja vacância automática do cargo eletivo no caso de não comparecimento a reuniões, as justificativas para as ausências deverão ser formalizadas e aceitas pelos demais membros do Conselho Fiscal.

<br>Art. 56 – No caso de vacância de cargo efetivo do Conselho Fiscal, será efetivado membro suplente, obedecida a ordem de maior votação. Havendo empate, a ordem de antiguidade da cooperativa singular associada à <strong>FNCC</strong>, na qual o suplente é cooperado.

<br>Art. 57 – Ocorrendo duas ou mais vagas no Conselho Fiscal, a Diretoria ou o restante dos seus membros convocarão as Assembleias Gerais para o devido preenchimento.</p>
                     <h6 class="fw-bold">SEÇÃO XI – DA REUNIÃO DO CONSELHO FISCAL</h6>
                       <p class="paragrafo-estatuto">Art. 58 – O Conselho Fiscal reunir-se-á ordinariamente, bimestralmente, em dia e hora previamente marcados, e extraordinariamente sempre que necessário, observando-se as seguintes normas:

<br>I. As reuniões se realizarão sempre com a presença dos 03 (três) membros efetivos ou dos suplentes previamente convocados;

<br>II. As deliberações serão tomadas pela maioria dos votos dos presentes;

<br>III. Os assuntos tratados e as deliberações tomadas constarão de atas lavradas no Livro de Atas do Conselho Fiscal, assinadas pelos presentes à reunião.

<br>§1º – As reuniões extraordinárias poderão ser convocadas por qualquer dos membros, por solicitação da Diretoria, pelo Comitê Executivo ou pela Assembleia Geral.

<br>§2º – Na primeira reunião, os membros efetivos do Conselho Fiscal escolherão entre si um coordenador incumbido de convocar e de dirigir os trabalhos das reuniões e um secretário para lavrar as atas.

<br>§3º – Na ausência do coordenador, os trabalhos serão dirigidos por um substituto escolhido na ocasião.

<br>§4º – Todos os membros do Conselho Fiscal terão direito à cédula de presença na forma estabelecida no Plano Financeiro e na Proposta de Rateio aprovados pela Assembleia Geral Ordinária.</p>
                       <h6 class="fw-bold">SEÇÃO XII – DA COMPETÊNCIA DO CONSELHO FISCAL</h6>
                       <p class="paragrafo-estatuto">Art. 59 – Compete ao Conselho:

<br>I. Examinar os negócios sociais, as receitas e as despesas, os pagamentos e os recebimentos das operações em geral e de outras questões financeiras e econômicas, verificando a adequação dos procedimentos adotados e a regularidade da escrituração;

<br>II. Verificar, mediante exame dos livros e atas e outros registros, se as decisões adotadas estão sendo corretamente implementadas;

<br>III. Observar se a Diretoria se reúne regularmente e se existem cargos vagos na composição daquele colegiado, que necessitem preenchimento;

<br>IV. Inteirar-se das obrigações da <strong>FNCC</strong> em relação às autoridades monetárias, fiscais, trabalhistas ou administrativas e às cooperativas singulares associadas e verificar se existem pendências para o adequado cumprimento;

<br>V. Avaliar os controles existentes relativos a valores e documentos sob custódia da <strong>FNCC</strong>;

<br>VI. Averiguar a atenção dispensada pelos Diretores Executivos às reclamações das cooperativas singulares associadas;

<br>VII. Analisar balancetes mensais e balanços gerais, demonstrativos de sobras e perdas, assim como o relatório de gestão e outros, emitindo parecer sobre esses documentos para a Assembleia Geral;

<br>VIII. Inteirar-se dos relatórios de auditoria e verificar se as observações neles contidas estão sendo devidamente consideradas pelos órgãos de administração e pelos gerentes;

<br>IX. Exigir, dos órgãos de administração ou de quaisquer de seus membros, relatórios específicos, declarações por escrito ou prestação de esclarecimentos, quando necessário;

<br>X. Apresentar à Diretoria, com periodicidade mínima trimestral, relatório contendo conclusões e recomendações decorrentes da atividade fiscalizadora;

<br>XI. Apresentar relatório sobre as atividades de a <strong>FNCC</strong> pronunciar-se sobre a regularidade dos atos praticados pela Diretoria e informar a Assembleia Geral Ordinária sobre eventuais pendências;

<br>XII. Instaurar inquéritos e comissões de averiguação mediante prévia anuência da Assembleia Geral;

<br>XIII. Convocar Assembleia Geral Extraordinária nas circunstâncias previstas neste Estatuto Social.</p>
                       <h6 class="fw-bold">SEÇÃO XIII – DO PROCESSO ELEITORAL</h6>
                       <p class="paragrafo-estatuto">Art. 60 – O processo eleitoral para o preenchimento dos cargos eletivos da <strong>FNCC</strong> obedecerá ao critério da maioria simples, ou em caso de aclamação ou voto secreto seguindo as normas regulamentadas e regulamentos próprios.</p>
                       <h6 class="fw-bold">SEÇÃO XIV – DA INELEGIBILIDADE DE CANDIDATOS A CARGOS DE ADMINISTRAÇÃO</h6>
                       <p class="paragrafo-estatuto">Art. 61 – São condições de inelegibilidade de candidatos a cargos dos órgãos de administração, inclusive os executivos eleitos:

<br>I. pessoas impedidas por lei;

<br>II. condenados à pena que vede, ainda que temporariamente, o acesso a cargos públicos;

<br>III. condenados por crime falimentar, de sonegação fiscal, de prevaricação, de suborno, de corrupção ativa ou passiva, de concussão, de peculato, ou contra a economia popular, a fé pública, a propriedade ou o Sistema Financeiro Nacional.

<br>Art. 62 ¬- Para se candidatarem a cargo político-partidário os membros ocupantes de cargos de administração deverão renunciar ao cargo ocupado na Cooperativa.</p>
                       <h5 class="fw-bold">CAPÍTULO VII – DA OUVIDORIA</h5>
<p class="paragrafo-estatuto">Art. 63 – A <strong>FNCC</strong> manterá, nos termos da legislação e normativos vigentes, componente organizacional único de Ouvidoria, para atendimento às demandas das singulares Associadas, visando atuar como canal de comunicação entre tais Cooperativas Associadas e seus associados, bem como com o intuito de possibilitar-lhes o registro de suas reclamações, de forma a assegurar a restrita observância das normas legais e regulamentares relativas ao direito do consumidor, atuando inclusive na mediação de conflitos.

<br>Art. 64 – Constituem atribuições da ouvidoria:

<br>I. Atender, registrar, instruir, analisar e dar tratamento formal e adequado às demandas dos clientes e usuários de produtos e serviços das singulares Associadas;

<br>II. Prestar os esclarecimentos necessários e dar ciência aos reclamantes acerca do andamento de suas demandas dos clientes e usuários de produtos e serviços e das providências adotadas;

<br>III. Informar aos reclamantes o prazo previsto para resposta final, o qual não pode ultrapassar 15 (quinze) dias, contados da data da protocolização da ocorrência;

<br>IV. Encaminhar resposta conclusiva para a demanda dos reclamantes até o prazo informado no inciso III deste artigo;

<br>V. Propor aos órgãos competentes das singulares associadas, medidas corretivas ou de aprimoramento de procedimentos e rotinas, em decorrência da análise das reclamações recebidas;

<br>VI. Elaborar e encaminhar ao Conselho de Administração ou Diretoria, ao final de cada semestre, relatório quantitativo e qualitativo acerca da atuação da ouvidoria, contendo as proposições de que trata o inciso anterior.

<br>§ 1º. O serviço prestado pela ouvidoria aos associados das singulares associadas deve ser identificado por meio de número de protocolo de atendimento.

<br>§ 2º. Os relatórios de que trata o inciso VI devem permanecer à disposição do Banco Central do Brasil pelo prazo mínimo de 05 (cinco) anos na sede das respectivas singulares associadas.

<br>Art. 65 – Os serviços de ouvidoria deverão ser disponibilizados aos associados das singulares aderentes de forma gratuita por meio de canais ágeis e eficazes por meio de termo de utilização da Ouvidoria assinado entre a <strong>FNCC</strong> e a singular associada;

<br>Art. 66 – Compete às singulares associadas dar ampla divulgação sobre a existência da ouvidoria, bem como de informações completas acerca da sua finalidade e forma de utilização.

<br>Art. 67 – É de responsabilidade das singulares associadas divulgar e manter atualizado o número telefônico para atendimento da ouvidoria em local e formato visível ao público no recinto das suas dependências, bem como nos respectivos sítios eletrônicos na internet e nos demais canais de comunicação utilizados para difundir os produtos e serviços da Cooperativa e, ainda em sistema de informações, na forma estabelecida pelo Banco Central do Brasil.

<br>Art. 68 – A designação e a destituição do Ouvidor são de competência da Diretoria da <strong>FNCC</strong>, devendo a escolha ser feita na primeira reunião ordinária após a sua posse e recair, necessariamente, sobre integrante dos quadros da <strong>FNCC</strong>.

<br>§ 1º. A designação do Ouvidor fica condicionada à comprovação de aptidão no exame de certificação, organizado por entidade de reconhecida capacidade técnica que abrangerá temas relacionados à ética, aos direitos do consumidor e à mediação de conflitos.

<br>§ 2º. Em caso de renúncia ou destituição do Ouvidor, deverá ser escolhido um novo integrante para o cargo na primeira reunião subsequente da Diretoria da <strong>FNCC</strong>.

<br>§ 3º. São motivos de destituição do cargo de Ouvidor a falta de exercício adequado de suas funções, nos termos deste Estatuto Social e do manual da ouvidoria, ou qualquer outro motivo que desabone a sua conduta para o exercício da função.

<br>§ 4º. A deliberação acerca da destituição do Ouvidor pode ocorrer a pedido de qualquer dos membros da Diretoria da <strong>FNCC</strong>, do Conselho Fiscal Efetivo ou de qualquer das singulares associadas, sendo que, neste último caso, o pedido deverá ser subscrito por pelo menos 3 (três) singulares associadas.

<br>Art. 69 – A função de Diretor responsável pela ouvidoria será exercida pelo Diretor Presidente, nos termos do disposto no art. 44, XVIII do presente Estatuto Social.

<br>Art. 70 – O Ouvidor e o Diretor Responsável pela ouvidoria responderão por todas as singulares Associadas que utilizarem o componente organizacional.</p>
                       <h5 class="fw-bold">CAPÍTULO VIII – DA DISSOLUÇÃO E LIQUIDAÇÃO</h5>
<p class="paragrafo-estatuto">Art. 71 – A <strong>FNCC</strong> poderá ser dissolvida voluntariamente, por deliberação da Assembleia Geral especialmente convocada para esse fim, por intermédio dos votos de, pelo menos 2/3 (dois terços) das cooperativas singulares associadas, salvo se 03 (três) cooperativas associadas se dispuserem a assegurar a continuidade.

<br>§1º – Quando a dissolução for deliberada pela Assembleia Geral, esta nomeará um liquidante, ou mais, e um Conselho Fiscal composto de 03 (três) membros para procederem a sua liquidação.

<br>§2º – A Assembleia Geral, nos limites de suas atribuições, poderá em qualquer época destituir os liquidantes, os membros do Conselho Fiscal, designando seus substitutos.

<br>§3º – Os liquidantes, investidos de todos os poderes normais de administração, devem proceder a liquidação conforme o disposto na legislação cooperativista.

<br>Art. 72 – A <strong>FNCC</strong> poderá ser, também, dissolvida:

<br>I. Quando houver alteração da sua forma jurídica;

<br>II. Quando houver a redução do número de cooperativas associadas a menos de 3 (três) ou do capital social a valor inferior ao previsto no Artigo 14, se, até a Assembleia Geral subsequente, realizada em prazo inferior a 6 (seis) meses, estes limites mínimos não forem restabelecidos;

<br>III. Quando houver a paralisação das atividades por mais de 120 (cento e vinte) dias corridos.

<br>Parágrafo Único – Nas hipóteses previstas neste Artigo, a dissolução da <strong>FNCC</strong> poderá ser promovida judicialmente, a pedido de qualquer cooperativa singular associada, caso a Assembleia Geral não a realize por iniciativa própria.

<br>Art. 73 – A dissolução da <strong>FNCC</strong> importará, também, no cancelamento de todos os registros junto às entidades do sistema cooperativista.</p>
                      <h5 class="fw-bold">CAPÍTULO VIX – DAS DISPOSIÇÕES GERAIS</h5>
<p class="paragrafo-estatuto">Art. 74 – Constituem condições básicas para o exercício dos cargos dos órgãos de administração e de Conselheiro Fiscal da <strong>FNCC</strong>, sem prejuízo de outras previstas em leis ou normas aplicadas ao cooperativismo de crédito:

<br>I. Pertencer a administração da cooperativa associada;

<br>II. Ter reputação ilibada;

<br>III. Não estar declarado inabilitado para cargos de administração de instituições financeiras e demais sociedades autorizadas a funcionar pelo Banco Central do Brasil ou em outras instituições sujeitas à autorização, ao controle e à fiscalização de órgãos e de entidades da administração pública, direta e indireta, incluídas as entidades de previdência privada, as sociedades de capitalização e as companhias abertas;

<br>IV. Não responder, nem qualquer empresa da qual seja controlador ou administrador, por pendências relativas ao protesto de títulos, cobranças judiciais, emissão de cheques sem fundo, inadimplemento de obrigações e outras ocorrências ou circunstâncias análogas;

<br>V. Não estar declarado falido ou insolvente, nem ter participado da administração ou ter controlado firma ou sociedade concordatária ou insolvente.

<br>§1º – Não podem compor a Diretoria Executiva, os parentes entre si até 2º (segundo) grau, em linha reta ou colateral.

<br>§2º – Não podem compor o Conselho Fiscal, os parentes dos diretores até o 2º (segundo) grau, em linha reta ou colateral, bem como os parentes entre si até esse grau.

<br>§3º – É vedada a participação nos órgãos administrativos, consultivos, fiscais e semelhantes da <strong>FNCC</strong>, ou nela exercer funções de gerência, pessoas que participem da administração ou detenham 5% (cinco por cento) ou mais do capital de empresas de fomento mercantil e de outras instituições financeiras e demais instituições autorizadas a funcionar pelo Banco Central do Brasil, com exceção de cooperativa de crédito e de instituições financeiras controladas por cooperativas de crédito.

<br>§4º – Os que participarem de ato em que se oculte a natureza das operações, podem ser declarados pessoalmente responsáveis pelas obrigações em nome dela contraídas, sem prejuízo das sanções penais cabíveis.

<br>Art. 75 – São inelegíveis, além das pessoas impedidas por lei, os condenados à pena que vede, ainda que temporariamente, o acesso a cargos públicos, ou condenados por crime falimentar, de sonegação fiscal, de prevaricação, de corrupção ativa ou passiva, de concussão, de peculato, contra a economia popular, a fé pública, a propriedade ou o Sistema Financeiro Nacional.

<br>Art. 76 – A cooperativa singular associada que possuir representante que componha qualquer órgão estatutário da <strong>FNCC</strong> e que, em qualquer operação, tiver interesse oposto ao da sociedade, não poderá participar das deliberações que sobre tal operação versarem.</p> 
             
